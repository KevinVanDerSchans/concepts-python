# Sets

"""
It is a data structure that represents an unordered collection of unique elements.

  - Unique elements: Sets do not allow duplicate elements.
  - They are not sorted and you cannot access the elements of a set by index.
  - They are created using {} braces or the set() function.
"""

my_set = set()
my_other_set = {}

print(type(my_set)) # <class 'set'>
print(type(my_other_set)) # <class 'dict'>

my_other_set = { 'Kevin', 'Schans', 29 }
