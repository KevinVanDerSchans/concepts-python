# Comparison operators

print(3 > 4)
print(3 < 4)

print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

print(3 > 4 == 2)

# Compare alphabetical order
print('ASD' < 'BSDF')
print('ASD' > 'BSDF')

# Compares the length of the string
print(len('ASDFM') > len('ASDF'))
print(len('ASD') > len('ASDF'))
